#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*cpy;

	cpy = lst;
	while (cpy != 0)
	{
		if (f != 0)
		{
			f(cpy->content);
			cpy = cpy->next;
		}
	}
}
